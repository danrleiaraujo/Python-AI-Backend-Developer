saldo = 200 #Atribuicao Simples
print(saldo) #200

saldo += 200 #Atribuicao com Adicao
print(saldo) #400

saldo /= 2 #Atribuicao com divisao com ponto flutuante
print(saldo) #200

saldo //= 2 #Atribuicao com divisao com inteiros
print(saldo) #100

saldo %= 80 #Atribuicao com modulo de resto de divisao
print(saldo) #20

saldo **= 2 #Atribuicao com elevacao
print(saldo) #400