saldo = 200
saque = 200

#Valor do saldo igual Valor do Saque
print(saldo == saque) #true

#Valor do saldo diferente Valor do Saque
print(saldo != saque) #false

#Valor do saldo maior Valor do Saque
print(saldo > saque) #false

#Valor do saldo maior ou igual Valor do Saque
print(saldo >= saque) #true

#Valor do saldo menor Valor do Saque
print(saldo < saque) #false

#Valor do saldo menor ou igual Valor do Saque
print(saldo <= saque) #true